package com.business.project.model.view;

public class RecordViewModel {

    private String name;
    private int amount;

    public RecordViewModel() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAmount() {
        return amount;
    }

    public void setAmount(int amount) {
        this.amount = amount;
    }
}
